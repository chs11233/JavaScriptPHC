package ut;
 
public class Ut {
	
	private int UtID;
	private String UtTitle;
	private String userID;
	private String UtDate;
	private String UtContent;
	private int UtAvailable;
	
	public int getUtID() {
		return UtID;
	}
	public void setUtID(int utID) {
		UtID = utID;
	}
	public String getUtTitle() {
		return UtTitle;
	}
	public void setUtTitle(String utTitle) {
		UtTitle = utTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUtDate() {
		return UtDate;
	}
	public void setUtDate(String utDate) {
		UtDate = utDate;
	}
	public String getUtContent() {
		return UtContent;
	}
	public void setUtContent(String utContent) {
		UtContent = utContent;
	}
	public int getUtAvailable() {
		return UtAvailable;
	}
	public void setUtAvailable(int utAvailable) {
		UtAvailable = utAvailable;
	}
	
	
	
}
