package wt;
 
public class Wt {
	
	private int WtID;
	private String WtTitle;
	private String userID;
	private String WtDate;
	private String WtContent;
	private int WtAvailable;
	
	public int getWtID() {
		return WtID;
	}
	public void setWtID(int wtID) {
		WtID = wtID;
	}
	public String getWtTitle() {
		return WtTitle;
	}
	public void setWtTitle(String wtTitle) {
		WtTitle = wtTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getWtDate() {
		return WtDate;
	}
	public void setWtDate(String wtDate) {
		WtDate = wtDate;
	}
	public String getWtContent() {
		return WtContent;
	}
	public void setWtContent(String wtContent) {
		WtContent = wtContent;
	}
	public int getWtAvailable() {
		return WtAvailable;
	}
	public void setWtAvailable(int wtAvailable) {
		WtAvailable = wtAvailable;
	}
	
	

}
